// typeof operator
console.log("----------typeof operator-------------");

let number = 9618211626;
let string = "Hello World";
let boolean = true; // false or True
let object = {}; // object is a Object Data Type
let array = []; // array is also a Object Data Type
let nulls = null; // nulls is a Object Data Type
let function_ = function () {
    console.log("Hello World");
}
let undefineds = undefined;




console.log(typeof number,number);
console.log(typeof string,string);
console.log(typeof boolean,boolean);
console.log(typeof object,object);
console.log(typeof array,array);
console.log(typeof nulls, nulls);
console.log(typeof function_, function_);
console.log(typeof undefineds, undefineds);


